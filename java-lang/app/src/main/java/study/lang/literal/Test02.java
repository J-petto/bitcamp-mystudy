package study.lang.literal;

// 다양한 진수법으로 표현해라.
//
public class Test02 {
    public static void main(String[] args) {
        System.out.println(100); // 10진수 -> 자주 씀
        System.out.println(0b01100100); // 2 진수
        System.out.println(0144); // 8진수
        System.out.println(0x064); // 16진수 -> 자주 씀 (전기적 표현으로 쓰고 싶을 때 2진수보다 간결하고 깔끔해서)
    }
}
